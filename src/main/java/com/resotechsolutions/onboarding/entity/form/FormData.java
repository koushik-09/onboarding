package com.resotechsolutions.onboarding.entity.form;

public class FormData{

    String field;
    Object data;

    public FormData() {
    }

    public String getField() {
        return field;
    }

    public void setField(String field) {
        this.field = field;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }
}
